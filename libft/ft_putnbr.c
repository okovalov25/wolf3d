#include "libft.h"

void				ft_putnbr(int n)
{
	unsigned int	i;
	char			c;

	if (n < 0)
	{
		write(1, "-", 1);
		i = -n;
	}
	else
		i = n;
	if (i <= 9)
	{
		c = i + '0';
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
}
